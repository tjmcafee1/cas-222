            <p>Watch the weather closely. The show goes on no matter what the weather is doing.
                You must wear your chip timing piece during the entire event. You will be given a band
                that will hold your chip timing piece around your ankle throughout the entire event.
                Be sure that it is snapped tightly. Be sure to have your Chip Timing piece on before you
                start the race and be sure to step over the timing mats after each segment of the race.
                If you are wearing a wet-suit, make sure the timing piece goes under your wetsuit
                otherwise, you will have a very difficult time getting off your wet suit.</p>

